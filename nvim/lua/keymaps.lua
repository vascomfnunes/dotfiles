-- LSP
-- ruby-lsp can't resolve some metaprogrammed DSL methods (`validates`,
-- `acts_as_list`, ...); when the LSP has no answer, fall back to the tags
-- generated by :GemTags.
local function definition_or_tag()
  local bufnr = vim.api.nvim_get_current_buf()
  local winid = vim.api.nvim_get_current_win()
  local cursor = vim.api.nvim_win_get_cursor(winid)
  local changedtick = vim.api.nvim_buf_get_changedtick(bufnr)
  local word = vim.fn.expand("<cword>")

  local function context_valid()
    return vim.api.nvim_buf_is_valid(bufnr)
      and vim.api.nvim_win_is_valid(winid)
      and vim.api.nvim_win_get_buf(winid) == bufnr
      and vim.api.nvim_buf_get_changedtick(bufnr) == changedtick
      and vim.deep_equal(vim.api.nvim_win_get_cursor(winid), cursor)
  end

  local function tag_fallback()
    if not context_valid() then return end
    -- LSP attach sets tagfunc back to the LSP; clear it so tjump reads the
    -- tags files instead.
    local root = vim.fs.root(vim.api.nvim_buf_get_name(bufnr), { "Gemfile", ".git" })
    local tag_file = require("tags").path(root)
    local ok = tag_file and pcall(vim.api.nvim_win_call, winid, function()
      require("tags").with_file(bufnr, tag_file, function()
        local tagfunc = vim.bo[bufnr].tagfunc
        vim.bo[bufnr].tagfunc = ""
        local jumped = pcall(vim.cmd.tjump, word)
        vim.bo[bufnr].tagfunc = tagfunc
        if not jumped then error("tag not found") end
      end)
    end)
    if not ok then
      vim.notify("No definition or tag for '" .. word .. "' (:GemTags to index gems)", vim.log.levels.WARN)
    end
  end
  local clients = vim.lsp.get_clients({ bufnr = 0, method = "textDocument/definition" })
  if #clients == 0 then return tag_fallback() end
  vim.lsp.buf_request_all(bufnr, "textDocument/definition", function(client)
    return vim.lsp.util.make_position_params(winid, client.offset_encoding)
  end, function(results)
    if not context_valid() then return end
    for _, res in pairs(results) do
      local r = res.result
      if r and (r.uri or not vim.tbl_isempty(r)) then
        vim.api.nvim_win_call(winid, function()
          vim.g.dotfiles_lazy.fzf()
          require("fzf-lua").lsp_definitions({ jump1 = true })
        end)
        return
      end
    end
    tag_fallback()
  end)
end
