-- AI (agentic.nvim)
-- Wrap agentic.nvim calls so the plugin only loads when an AI mapping is used.
local function agentic(fn, ...)
  local args = { ... }
  return function()
    vim.g.dotfiles_lazy.agentic()
    require("agentic")[fn](unpack(args))
  end
end

